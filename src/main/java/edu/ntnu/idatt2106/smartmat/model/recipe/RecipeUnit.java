package edu.ntnu.idatt2106.smartmat.model.recipe;

/**
 * Enum representing different type of units in a recipe.
 * @author Simen G.
 * @version 1.0 - 20.4.2023
 */
public enum RecipeUnit {
  /**
   * Unit enum for kilo.
   */
  KILO,
  /**
   * Unit enum for liter.
   */
  LITER,
  /**
   * Unit enum for gram.
   */
  GRAM,
  /**
   * Unit enum for deciliter.
   */
  DL,
  /**
   * Unit enum for milliliter.
   */
  ML,
  /**
   * Unit enum for teaspoon.
   */
  TEASPOON,
  /**
   * Unit enum for tablespoon.
   */
  TABLESPOON,
}
